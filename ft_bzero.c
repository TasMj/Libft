#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char		*ss;
	size_t		i;

	i = 0;
	ss = (char *)s;
	{
		while (i < n)
		{
			ss[i] = '\0';
			i++;
		}
	}
}
